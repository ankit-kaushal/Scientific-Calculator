    private void setZoomType(double magnitude, char zoomType)
    {
        //zoomType
        //+ = zoom in
        //- is zoom out
        //s is standard
        //t is trigonometric
        //# is statistic
        xMinimum=Xmin;
        xMaximum=Xmax;
        yMinimum=Ymin;
        yMaximum=Ymax;
  
        switch(zoomType)
        {
            case '+':
                xMinimum/=magnitude;
                xMaximum/=magnitude;
                yMinimum/=magnitude;
                yMaximum/=magnitude;
                break;
            case '-':
                xMinimum*=magnitude;
                xMaximum*=magnitude;
                yMinimum*=magnitude;
                yMaximum*=magnitude;
                break;
            case 's':
                xMinimum=-10;
                xMaximum=10;
                yMinimum=-10;
                yMaximum=10;
                break;
            case 't':
                xMinimum=Math.PI*-2;
                xMaximum=Math.PI*2;
                yMinimum=-2;
                yMaximum=2;
                break;
            case '#':
                xMinimum=0;
                xMaximum=10;
                yMinimum=0;
                yMaximum=10;
                break;
            default:
                JOptionPane.showMessageDialog(null,"Error occured while attempting to zoom");
        }
        
        //write to bounds text fields
        DecimalFormat DForm = new DecimalFormat("#.##");
        xMinimum=Double.valueOf(DForm.format(xMinimum));
        xMaximum=Double.valueOf(DForm.format(xMaximum));
        yMinimum=Double.valueOf(DForm.format(yMinimum));
        yMaximum=Double.valueOf(DForm.format(yMaximum));
        
        //graph with new window bounds
        plot();
    }
    
